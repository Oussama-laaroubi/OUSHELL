bool	is_n_option(const char *str)
{
	if (str[0] != '-')
	{
		return (false);
	}
	for (int i = 1; str[i] != '\0'; i++)
	{
		if (str[i] != 'n')
		{
			return (false);
		}
	}
	return (true);
}
